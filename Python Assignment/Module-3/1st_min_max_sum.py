# Write a Python function to get the largest number, smallest num and sum of all from a list.

list=[5,9,7,8,4,6,3,2,1]    # creating list 

a=max(list)     # max() function will find maximum number from the list.
b=min(list)     # min() function will find minimum number from the list.
c=sum(list)     # sum() function will give the total of the list.

print("The maximum number in the list is: ",a)
print("The minimum number in the list is: ",b)
print("The sum of the list is: ",c)